SELECT 
    *
FROM
    shirts
WHERE
    shirt_size = 'M';
    
-- UPDATE ALL POLO SHIRTS TO SIZE LARGE
UPDATE shirts 
SET 
    shirt_size = 'L'
WHERE
    article = 'polo shirt'
;

-- UPDATE THE SHIRT LAST WORN 15 DAYS AGO TO ZERO
UPDATE shirts 
SET 
    last_worn = 0
WHERE
    last_worn = 15
;

-- UPDATE ALL WHITE SHIRTS TO SIZE OF XS AND COLORS TO OFF WHITE
UPDATE shirts 
SET 
    shirt_size = 'XS',
    color = 'off white'
WHERE
    color = 'white'
;

-- DELETE ALL SHIRTS LAST WORN 200 DAYS AGO
DELETE FROM shirts 
WHERE
    last_worn = 200;

-- DELETE ALL TANK TOPS
DELETE FROM shirts 
WHERE
    article = 'tank top';
    
